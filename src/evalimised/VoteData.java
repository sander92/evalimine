public class VoteData {

    private int id;
    private String name;
    private String party;
    private String area;
    private int votes;
    
    void setId(int i){
    	id = i;
    }
    void setName(String s){
    	name = s;
    }
    void setParty(String s){
    	party = s;
    }
    void setArea(String s){
    	area = s;
    }
    void setVotes(int i){
    	votes = i;
    }
}
